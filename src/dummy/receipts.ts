function hashSeed(seed: string): number {
  let h = 1779033703 ^ seed.length;
  for (let i = 0; i < seed.length; i += 1) {
    h = Math.imul(h ^ seed.charCodeAt(i), 3432918353);
    h = (h << 13) | (h >>> 19);
  }
  return h >>> 0;
}

function createRng(seed: string) {
  let state = hashSeed(seed) || 0x1f123bb5;
  return () => {
    state = (state + 0x6d2b79f5) >>> 0;
    let t = Math.imul(state ^ (state >>> 15), 1 | state);
    t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

function getReviewMedia(seed: string): ReviewMedia[] {
  const rng = createRng(seed);
  const pool = [...REVIEW_MEDIA_POOL];
  const selected: ReviewMedia[] = [];
  const mediaCount = Math.floor(rng() * 4); // 0-3 media items

  for (let i = 0; i < mediaCount && pool.length > 0; i += 1) {
    const index = Math.floor(rng() * pool.length);
    const [item] = pool.splice(index, 1);
    if (item) {
      selected.push({ ...item, id: `${item.id}-${seed}` });
    }
  }

  if (selected.length > 0 && rng() > 0.7) {
    const video = REVIEW_VIDEO_POOL[Math.floor(rng() * REVIEW_VIDEO_POOL.length)];
    if (video) {
      selected.splice(1, 0, { ...video, id: `${video.id}-${seed}` });
    }
  }

  return selected;
}

